public class increment_decrement_prac {
    public static void main(String[] args) {
        int i = 10;
        int j = i++;
        int k = ++i;
        System.out.println(j);
        System.out.println(i);
        System.out.println(k);
        int m = i--;
        System.out.println(m);    
        System.out.println(i);
    }
}
